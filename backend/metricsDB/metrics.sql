-- Create the applications table with additional fields for app logos and app types
CREATE TABLE IF NOT EXISTS applications (
  app_id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  app_name VARCHAR(255) NOT NULL,
  version_no VARCHAR(255) NOT NULL,
  provider VARCHAR(255) NOT NULL,
  release_date DATE NOT NULL,
  build_no INT NOT NULL,
  no_instances INT NOT NULL,
  description VARCHAR(255) NOT NULL,
  saved TINYINT(1) NOT NULL,
  app_logo VARCHAR(255) NOT NULL,  -- New field for app logos
  app_type VARCHAR(255) NOT NULL   -- New field for app types (e.g., Backend, Frontend, Web, etc.)
);

-- Create the instances table with a foreign key reference to the applications table
CREATE TABLE IF NOT EXISTS instances (
  instance_id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  country VARCHAR(255) NOT NULL,
  instance_name VARCHAR(255) NOT NULL,
  code VARCHAR(255) NOT NULL,
  app_id INT NOT NULL,
  FOREIGN KEY (app_id) REFERENCES `applications`(app_id) ON DELETE CASCADE
);

-- CREATE TABLE IF NOT EXISTS `instances` (
--   instance_id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
--   country VARCHAR(255) NOT NULL,
--   instance_name VARCHAR(255) NOT NULL,
--   code VARCHAR(255) NOT NULL,
--   app_id INT NOT NULL,
--   FOREIGN KEY (app_id) REFERENCES applications(app_id) ON DELETE CASCADE
-- );

CREATE TABLE IF NOT EXISTS `metrics` (
  metric_id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  metric VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS `alerts` (
  alert_id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  metric_id INT NOT NULL,
  instance_id INT NOT NULL,
  threshold INT NOT NULL,
  FOREIGN KEY (metric_id) REFERENCES `metrics`(metric_id) ON DELETE CASCADE,
  FOREIGN KEY (instance_id) REFERENCES `instances`(instance_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS `app_metrics` (
  metric_id INT NOT NULL,
  app_id INT NOT NULL,
  PRIMARY KEY (metric_id, app_id),
  FOREIGN KEY (metric_id) REFERENCES metrics(metric_id) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (app_id) REFERENCES applications(app_id) ON DELETE CASCADE ON UPDATE CASCADE
);

